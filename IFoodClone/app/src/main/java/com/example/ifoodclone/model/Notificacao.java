package com.example.ifoodclone.model;

public class Notificacao {

    private String title;
    private String body;

    public Notificacao() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
